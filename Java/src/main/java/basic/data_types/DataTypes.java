/*
Two types of data types are there

1. Primitive Data Type: These are the basic building blocks that store simple values directly in memory.
 Examples of primitive data types are boolean, char, byte, short, int, long, float, and double.
 --> Stored on the stack
 --> Primitive data types are faster

2. Non-Primitive Data Types (Object Types): These are reference types that store memory addresses of objects.
 Examples of Non-primitive data types are String, Array, Class, Interface, and Object.
 --> Stored on the heap
 --> Non-primitive data types are slower

*/